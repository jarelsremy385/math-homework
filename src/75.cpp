#include <iostream>
using namespace std;

int main() {
    int a = 5;
    int b = 10;
    int c = 20;
    
    if (a > b) {
        cout << "The value of a is greater than b." << endl;
    } else {
        cout << "The value of a is not greater than b." << endl;
    }
    
    if (c == 20) {
        cout << "C is equal to 20." << endl;
    } else {
        cout << "C is not equal to 20." << endl;
    }
    
    return 0;
}
