#include <iostream>
using namespace std;

int main() {
    int x = 5;
    double y = 3.141592653589793;
    float z = 10.0f; // Assuming a fixed precision for floating-point operations

    cout << "x = " << x << endl;
    cout << "y = " << y << endl;
    cout << "z = " << z << endl;

    return 0;
}
